// Step 1: Create an array of student objects
let students = [
  { rollNo: 101, name: "Ali", marks: 85 },
  { rollNo: 102, name: "Ahmed", marks: 72 },
  { rollNo: 103, name: "Sara", marks: 90 },
  { rollNo: 104, name: "Fawad", marks: 95 }
];

// Step 2: Ask user for roll number and name
let inputRoll = parseInt(prompt("Enter your Roll Number:"));
let inputName = prompt("Enter your Name:");

// Step 3: Check if student exists
let found = false;

for (let i = 0; i < students.length; i++) {
  if (students[i].rollNo === inputRoll && students[i].name.toLowerCase() === inputName.toLowerCase()) {
    alert(`✅ Student Found!\nName: ${students[i].name}\nMarks: ${students[i].marks}`);
    found = true;
    break;
  }
}

if (!found) {
  alert("❌ Student not found! Please check your roll number or name.");
}